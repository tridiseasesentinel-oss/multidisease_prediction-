from sklearn.model_selection import train_test_split
from sklearn.preprocessing import LabelEncoder

def get_test_split(csv_path, target):
    df = pd.read_csv(csv_path).dropna()
    le = LabelEncoder()

    for col in df.select_dtypes(include=['object']).columns:
        if col != target:
            df[col] = le.fit_transform(df[col].astype(str))

    X = df.drop([target], axis=1)
    if 'id' in X.columns: X = X.drop('id', axis=1)
    if 'Diabetes_binary' in X.columns: X = X.drop('Diabetes_binary', axis=1)

    y = le.fit_transform(df[target].astype(str))

    # Extract the exact 20% validation split
    _, X_test, _, y_test = train_test_split(X, y, test_size=0.20, random_state=42)
    return X_test, y_test, le

