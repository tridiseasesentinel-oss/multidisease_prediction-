    # 1. Convert dictionary to DataFrame
    df = pd.DataFrame([raw_data_dict])

    # 2. Apply same label encoding (use the saved encoders from training)
    for col in df.select_dtypes(include=['object']).columns:
        # Use .transform(), NOT .fit_transform()
        df[col] = label_encoder_objects[col].transform(df[col].astype(str))

    # 3. Ensure columns match the training order exactly
    df = df.reindex(columns=features_list)

    # 4. Fill missing columns with 0 (if any were missing in user input)
    df = df.fillna(0)

    return df

